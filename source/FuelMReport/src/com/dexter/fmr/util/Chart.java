package com.dexter.fmr.util;

public class Chart
{
	private String period;
	private double value;
	
	public Chart()
	{}

	public String getPeriod() {
		return period;
	}

	public void setPeriod(String period) {
		this.period = period;
	}

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}
}
